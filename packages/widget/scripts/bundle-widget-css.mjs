#!/usr/bin/env node
/**
 * Concatenates `src/styles/_*.css` source files into `src/styles/widget.css`
 * in the order listed in `BUNDLE_ORDER`.
 *
 * Why: tsup loads `widget.css` as text and Vite imports it as raw / CSS — both
 * paths need a single physical file with all rules inlined (no @import).
 *
 * Run via `npm run build:css` (also wired into `prebuild`).
 */
import { readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const STYLES = resolve(__dirname, "..", "src", "styles");
const OUT = resolve(STYLES, "widget.css");

const BUNDLE_ORDER = [
  "_tokens.css",
  "_layout-helpers.css",
  "_base.css",
  "_chrome.css",
  "_controls.css",
  "_badge-notice.css",
  "_pari.css",
  "_bet.css",
  "_order-book.css",
  "_chart.css",
  "_dialog.css",
  "_utilities.css",
  "_accessibility.css",
];

const HEADER = `/* ===== TONCAST WIDGET STYLES =====
   Generated by scripts/bundle-widget-css.mjs from src/styles/_*.css.
   Do NOT edit this file by hand — edit the source modules instead.
   ================================ */
`;

async function main() {
  const parts = [HEADER];
  for (const file of BUNDLE_ORDER) {
    const text = await readFile(resolve(STYLES, file), "utf8");
    parts.push(`\n/* ----- ${file} ----- */\n`);
    parts.push(text.trimEnd());
    parts.push("\n");
  }
  const out = parts.join("");
  if (process.argv.includes("--check")) {
    const current = await readFile(OUT, "utf8");
    if (current !== out) {
      console.error(
        "[bundle-widget-css] widget.css is out of sync with _*.css sources.\n" +
          "Run `npm run build:css` to regenerate.",
      );
      process.exit(1);
    }
    return;
  }
  await writeFile(OUT, out, "utf8");
  console.log(`[bundle-widget-css] wrote ${OUT} (${out.length} bytes)`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
